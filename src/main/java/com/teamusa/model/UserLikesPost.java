package com.teamusa.model;

public class UserLikesPost {
	
	private int user;
	private int post;
	
	public UserLikesPost(int user, int post) {
		this.user = user;
		this.post = post;
	}
	/**
	 * @return the user
	 */
	public int getUser() {
		return user;
	}
	/**
	 * @param user the user to set
	 */
	public void setUser(int user) {
		this.user = user;
	}
	/**
	 * @return the post
	 */
	public int getPost() {
		return post;
	}
	/**
	 * @param post the post to set
	 */
	public void setPost(int post) {
		this.post = post;
	}

}
