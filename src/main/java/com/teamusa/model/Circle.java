package com.teamusa.model;

/**
 * @author phillipninan
 *
 */
public class Circle 
{
	private int circleID;
	private String circleName;
	private int ownerOfCircle;
	private String type;
	
	public Circle(int circleID, String circleName, int ownerOfCircle, String type) {
		this.circleID = circleID;
		this.circleName = circleName;
		this.ownerOfCircle = ownerOfCircle;
		this.type = type;
	}
	/**
	 * @return the circleID
	 */
	public int getCircleID() {
		return circleID;
	}
	/**
	 * @param circleID the circleID to set
	 */
	public void setCircleID(int circleID) {
		this.circleID = circleID;
	}
	/**
	 * @return the circleName
	 */
	public String getCircleName() {
		return circleName;
	}
	/**
	 * @param circleName the circleName to set
	 */
	public void setCircleName(String circleName) {
		this.circleName = circleName;
	}
	/**
	 * @return the ownerOfCircle
	 */
	public int getOwnerOfCircle() {
		return ownerOfCircle;
	}
	/**
	 * @param ownerOfCircle the ownerOfCircle to set
	 */
	public void setOwnerOfCircle(int ownerOfCircle) {
		this.ownerOfCircle = ownerOfCircle;
	}
	/**
	 * @return the type
	 */
	public String getType() {
		return type;
	}
	/**
	 * @param type the type to set
	 */
	public void setType(String type) {
		this.type = type;
	}
	
	
}
